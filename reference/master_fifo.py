# ‚îÄ‚îÄ‚îÄ FIFOTracker with replay support ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
class FIFOTracker:
    def __init__(self):
        # key ‚Üí deque of [qty, price]
        self.lots = {}
        self.logs = []

    def process(self, fund_id, wallet, asset, side, qty, price_usd, date, tx_hash, log=False):
        """ Process one leg. If log=False, rebuild lots w/o recording. """
        # normalize
        try:
            q = Decimal(str(qty))
            p = Decimal(str(price_usd))
        except (InvalidOperation, TypeError):
            return

        key = (fund_id, wallet, asset)
        dq = self.lots.setdefault(key, deque())

        proceeds = cost_basis = gain = Decimal("0")
        if side == "buy":
            dq.append([q, p])
        else:  # sell
            to_sell = abs(q)
            while to_sell > 0 and dq:
                lq, lp = dq[0]
                take = min(lq, to_sell)
                proceeds   += take * p
                cost_basis += take * lp
                gain       += (take * p - take * lp)
                lq -= take; to_sell -= take
                if lq > 0:
                    dq[0][0] = lq
                else:
                    dq.popleft()
            if to_sell > 0:
                # short
                proceeds   += to_sell * p
                cost_basis += to_sell * p
                dq.appendleft([-to_sell, p])

        remaining_qty  = sum(l for l,_ in dq)
        remaining_cost = sum(l * pr for l,pr in dq)

        if log:
            self.logs.append({
                "fund_id":                  fund_id,
                "wallet_address":           wallet,
                "asset":                    asset,
                "date":                     date,
                "hash":                     tx_hash,
                "side":                     side,
                "qty":                      q,
                "eth_usd_price":           p,
                "proceeds_usd":             proceeds,
                "cost_basis_sold_usd":      cost_basis,
                "realized_gain_usd":        gain,
                "remaining_qty":            remaining_qty,
                "remaining_cost_basis_usd": remaining_cost
            })

    def to_dataframe(self):
        return pd.DataFrame(self.logs)

