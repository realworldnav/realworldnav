df_master_rebuilt["unit_price_eth"] = df_master_rebuilt.apply(compute_unit_price_eth, axis=1)
df_master_rebuilt["unit_price_usd"] = df_master_rebuilt.apply(compute_unit_price_usd, axis=1)

