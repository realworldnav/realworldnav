def get_token_eth_price_at_block(w3, token_symbol, block_number):
    """
    Return token/ETH price at given block.
    For stablecoins, price is $1 / ETH price from Chainlink.
    """
    STABLECOINS = {"USDC", "USDT", "DAI"}
